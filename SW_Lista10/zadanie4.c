#define SPI_DDR DDRB
#define SPI_PORT PORTB
#define SPI_MOSI PB3
#define SPI_SCK PB5
#define SPI_LA PB1
#define SPI_OE PB2

void spi_init(){
    //ustaw piny MOSI, SCK i ~SS jako wyjÅ›cie
    SPI_DDR |= _BV(SPI_MOSI) | _BV(SPI_SCK) | _BV(SPI_OE);
    SPI_PORT |= _BV(SPI_OE);
    //wlaczenie zegara na 125 kHz i wlazcenie trybu master  w SPI
    SPCR = _BV(SPE) | _BV(MSTR) | _BV(SPR1) | _BV(SPR0); 
}

void spi_transfer(uint8_t data){
    SPI_PORT &= ~_BV(SPI_OE);
    uint8_t byte = 0x30;
    byte |= (data & 0xf0)>>4;
    SPDR = byte;
    while(!(SPSR & _BV(SPIF)));
    SPSR |= _BV(SPIF);
    byte = (data << 4) & 0xF0;
    SPDR = byte;
    while(!(SPSR & _BV(SPIF)));
    SPSR |= _BV(SPIF);
    SPI_PORT |= _BV(SPI_OE);
}



